public class Operators {
    public static void main(String[] args) {
        int a = 6;
        int b = 3;

        double sum = a + b;
        double mult = a * b;
        double sub = a - b;
        double div = (double) a / b;
        

        System.out.println(sum + " " + mult + " " + sub + " " + div);
    }
}
